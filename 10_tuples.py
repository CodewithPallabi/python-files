#tuples in python.
#tuples are immutable, ordered collection of items.

s = (1, 2, 3, 4, 5,5,5)
print(s)
print(type(s))

#accessing elements in a tuple
print(s[0]) #first element
print(s[1]) #second element
print(s[-1]) #last element
print(s.count(5)) #count of 5 in the tuple
print(s.index(3)) #index of first occurrence of 3

a = (1,)   #single element tuple, note the comma



