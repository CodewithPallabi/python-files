#operators in python

a = 5
b = 10
#all these are arithmetic operators
print(a+b) #addition
print(a-b) #subtraction
print(a*b) #multiplication
print(a/b) #division
print(a//b) #floor division
print(a%b) #modulus
print(a**b) #exponentiation


#assignment operators
c = 5
c += 5 #c = c + 5
print(c)           #output will be 10 because c is 5 and we are adding 5 to it
c -= 5 #c = c - 5  
print(c)           #output will be 5 because c is 10 and we are subtracting 5 from it
c *= 5 #c = c * 5
print(c)            #output will be 25 because c is 5 and we are multiplying it by 5
c /= 5 #c = c / 5
print(c)            #output will be 5.0 because c is 25 and we are dividing it by 5


#comparison operators
x = 5
y = 10
print(x == y) #equal to
print(x != y) #not equal to
print(x > y) #greater than
print(x < y) #less than
print(x >= y) #greater than or equal to
print(x <= y) #less than or equal to


#logical operators
p = True
q = False
print(p and q) #logical AND
print(p or q) #logical OR

