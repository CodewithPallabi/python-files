#variables are used to store data in python. A variable is a name that refers to a value. In python, we can create a variable by assigning a value to it using the assignment operator (=). The value can be of any data type such as integer, float, string, list, tuple, dictionary, etc. Variables are case-sensitive and can contain letters, numbers, and underscores. They cannot start with a number and cannot contain spaces. We can also reassign a variable to a new value at any time in the program.

a = "pallabi" #string variable
b = 25 #integer variable
c = 3.14 #float variable
d= [1, 2, 3, 4, 5] #list variable
e = (1, 2, 3, 4, 5) #tuple variable
f = {"name": "pallabi", "age": 25} #dictionary variable
g = True #boolean variable
h = None #None variable

print(type(a)) #to check the type of variable
print(type(b))     
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
